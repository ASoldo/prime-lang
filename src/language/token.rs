use crate::language::span::Span;

#[derive(Clone, Debug, PartialEq)]
pub struct Token {
    pub kind: TokenKind,
    pub span: Span,
}

#[derive(Clone, Debug, PartialEq)]
pub enum TokenKind {
    Identifier(String),
    Integer(i128),
    Float(f64),
    String(String),
    Rune(char),
    TemplateString(String),

    Fn,
    Let,
    Mut,
    Const,
    Struct,
    Enum,
    Interface,
    Impl,
    Import,
    Using,
    ModuleKw,
    LibraryKw,
    TestKw,
    Export,
    Macro,
    Return,
    If,
    Else,
    For,
    In,
    While,
    Loop,
    Match,
    Break,
    Continue,
    Defer,
    Spawn,
    Async,
    Await,
    Try,
    Move,
    Pub,
    True,
    False,
    As,

    Ampersand,
    AmpersandAmpersand,
    PipePipe,
    Pipe,
    Caret,
    Bang,
    BangEq,
    Eq,
    EqEq,
    Lt,
    LtEq,
    Gt,
    GtEq,
    Plus,
    Minus,
    Star,
    Slash,
    Percent,
    Dot,
    DotDot,
    DotDotEq,
    Hash,
    Comma,
    Colon,
    ColonColon,
    Semi,
    Question,
    At,
    Tilde,

    Arrow,    // ->
    FatArrow, // =>

    LParen,
    RParen,
    LBrace,
    RBrace,
    LBracket,
    RBracket,

    Eof,
}

impl TokenKind {}
